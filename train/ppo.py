"""PPO policy training from self-play decision records (SOT-1689).

Learns the small policy+value MLP that :class:`agents.ppo_agent.PPOAgent` plays,
from the JSONL decision records of the SOT-1688 self-play pipeline
(:mod:`eval.selfplay`, schema ``ume-selfplay-v1``), and writes the weights to
``data/policy.json`` (schema ``ume-policy-v1``, see :mod:`agents.policy_net`).

**Local training tool only.** This is the one place numpy is allowed (see the
training-only section of ``requirements.txt``); nothing under ``agents/`` may
import it. The pure-Python forward in :mod:`agents.policy_net` and the numpy
forward here implement the same math and must stay in lockstep.

Algorithm
---------
Vanilla PPO: clipped surrogate objective + GAE(γ, λ), Adam, a value-MSE head and
an entropy bonus, over K epochs of minibatches per update. The training loop
(scaled up by SOT-1695: multi-deck rotation, RuleAgent sparring mix,
per-iteration evaluation with a best-iteration promotion gate and stagnation
early-stop; SOT-1699 adds **opponent-pool "league" self-play** — sparring vs a
bounded FIFO of past-policy snapshots, learner-side records only — and bounded
**reward shaping** — potential-based prize-differential + a deck-out/no-active
loss penalty, both applied at training time so the stored records stay
schema-valid ±1/0):

1. play ``--games-per-iter`` matches with the *current* policy through
   :func:`eval.selfplay.run_selfplay` — mirror games (optionally rotating the
   ``--deck-dir`` decks) plus a ``--sparring-frac`` share vs the RuleAgent
   baseline (each iteration's records are kept as a JSONL under ``--data-dir``
   — the same audited pipeline/schema as SOT-1688);
2. rebuild trajectories per ``(game, player)``; the reward is 0 everywhere and
   the terminal ±1/0 on a trajectory's last decision;
3. recompute values and the acting policy's log-probs from the stored features
   under the **pre-update snapshot** (the records were generated by exactly
   these weights, so the recomputation *is* π_old — no schema change needed);
4. run the clipped PPO update and save the artifact.

Records train the distribution of a decision's **first chosen option index**
(``action_index``) — the exact quantity :func:`agents.policy_net.sample_action`
samples via Gumbel top-k. Decisions with no real choice (``n_options < 2``),
empty actions, or an index beyond the slot head are skipped.

再学習可能: ``--init-from`` resumes from an existing ``policy.json``, and
``--bootstrap-data`` warm-starts from any existing record file (e.g. the
SOT-1688 rule-mirror acceptance data) before the self-play iterations — with
π_old recomputed under the current weights this is an advantage-weighted
imitation update, useful to escape the random init. ``--iters 0`` with
``--bootstrap-data`` is therefore a pure offline (engine-free) training run.

CLI::

    venv/bin/python train/ppo.py --iters 6 --games-per-iter 32 \
        --bootstrap-data eval/selfplay_runs/acceptance_sot1688.jsonl \
        --out data/policy.json
"""

